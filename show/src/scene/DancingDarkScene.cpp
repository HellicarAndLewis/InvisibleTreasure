DancingDarkScene::DancingDarkScene() {
    name = "Dancing in the Dark";
}

void DancingDarkScene::setup() {
    // subscenes
    subsceneStart = 67;
    subsceneEnd = 76;
    SceneBase::setup();
}

void DancingDarkScene::update() {
    if (mode==AppModel::SLAVE) {}
    else if (mode==AppModel::WINDOW) {}
    else if (mode==AppModel::MASTER) {}
    SceneBase::update();
}

