#include "LedDisplay.h"

LedDisplay::LedDisplay() {
}

void LedDisplay::setup() {
    font.loadFont("fonts/led_board-7.ttf", 50);
}

void LedDisplay::update() {
}

void LedDisplay::draw() {
    ofSetColor(250);
    ofRect(0, 0, ofGetWidth(), ofGetHeight());
    ofSetColor(20);
    string s = ofToString(ofGetElapsedTimeMillis());
    ofRectangle rect = font.getStringBoundingBox(s, 0, 0);
    font.drawString(s, (ofGetWidth()/2) - (rect.width/2), ofGetHeight()/2);
}

void LedDisplay::exit() {
}

